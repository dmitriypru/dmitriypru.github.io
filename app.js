const tokenContract = new web3.eth.Contract(abi, contractAddress);

window.addEventListener('load', async () => {
    const tokenName = await tokenContract.methods.name().call();
    const tokenSymbol = await tokenContract.methods.symbol().call();
    const tokenSupply = await tokenContract.methods.totalSupply().call();

    document.getElementById('tokenName').innerText = tokenName;
    document.getElementById('tokenSymbol').innerText = tokenSymbol;
    document.getElementById('tokenSupply').innerText = tokenSupply;

    const addresses = ['0x...', '0x...']; // to be replaced with addresses
    const holdersList = document.getElementById('tokenHolders');
    for (let address of addresses) {
        const balance = await tokenContract.methods.balanceOf(address).call();
        const listItem = document.createElement('li');
        listItem.innerText = `${address}: ${balance}`;
        holdersList.appendChild(listItem);
    }
});

function transferTokens(message) {
    const recipient = document.getElementById('recipientAddress').value;
    const amount = document.getElementById('transferAmount').value;

    web3.eth.getAccounts().then(accounts => {
        const sender = accounts[0];
        tokenContract.methods.transfer(recipient, amount).send({ from: sender })
            .on('receipt', receipt => {
                alert('Transfer successful!');
            })
            .on('error', error => {
                alert('Transfer failed:', error);
            });
    });
}